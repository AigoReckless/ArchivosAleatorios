    private void registroButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registroButtonActionPerformed
        try{
            File ff = new File("ficheroAleatorio.txt");
            RandomAccessFile aa = new RandomAccessFile(ff, "rw");
            
            String nom = alumnoTF.getText();
            float parcial1 = Float.parseFloat(parcial1TF.getText());
            float parcial2 = Float.parseFloat(parcial2TF.getText());
            float talleres = Float.parseFloat(talleresTF.getText());
            float proyecto = Float.parseFloat(proyectoTF.getText());
            char nombre[] = new char[25]; //Creamos el array del nombre
            
            for(int i=0; i< nom.length(); i++){
                nombre[i] = nom.charAt(i);
            }
            /*
            El resto de valores estarÃ¡n vacios
            */
            
            //Creamos un objeto registro nuevo y le damos valores
            Registro reg = new Registro(nombre,parcial1,parcial2,talleres,proyecto);
            
            //Se llama a los procedimientos y finalizamos el programa
            
            Registro.grabarRegistro(aa, reg, ff);
            Registro.leerDatos(aa, ff);
            
        
        }catch(IOException ioex){
            
        }
    }//GEN-LAST:event_registroButtonActionPerformed

